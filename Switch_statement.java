public class Switch_statement {
    public static void main(String[] args){
        String day = "wed";
        switch (day){
            case "mon":
                System.out.println("Just a boring day after weekend");
            break;

            case "tue":
                System.out.println("laern automation man");
            break;

            case "wed":
                System.out.println("its time to get sad");
            break;
            default:
                System.out.println("i am default");
        }
    }
}
