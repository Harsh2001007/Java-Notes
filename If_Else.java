public class If_Else {
    public static void main(String[] args){
        int x = 5;
        if (x>10){
            System.out.println("x is smaller then 10");
        }
        else if(x != 5){
            System.out.println("x is equal to 5");
        }
        else{
            System.out.println("x is less then 10");
        }
    }
}
